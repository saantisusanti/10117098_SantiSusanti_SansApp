package com.santisusanti.a10117098_santisusanti_sansapp.Model

class Person {
    var nim:String?=null
    var nama:String?=null
    var kelas:String?=null
    var telp:String?=null
    var email:String?=null
    var sosmed:String?=null

    constructor(nim:String,nama:String,kelas:String,telp:String,email:String,sosmed:String){
        this.nim=nim
        this.nama=nama
        this.kelas=kelas
        this.telp=telp
        this.email=email
        this.sosmed=sosmed
    }
}